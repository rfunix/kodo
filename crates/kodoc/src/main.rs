//! # kodoc -- The Kodo Compiler
//!
//! The command-line interface for the Kodo programming language compiler.
//! Designed to be used both by AI agents (with `--emit json-errors`) and
//! humans (with beautiful terminal error messages via ariadne).

mod audit;
mod certificate;
mod commands;
mod diagnostics;
mod embedded_runtime;
mod explanations;
mod formatter;
mod repl;

use clap::{Parser, Subcommand};
use std::path::PathBuf;

/// The Kodo compiler -- a language built for AI agents, transparent for humans.
#[derive(Parser)]
#[command(name = "kodoc", version, about, long_about = None)]
struct Cli {
    /// Subcommand to execute.
    #[command(subcommand)]
    command: Command,
}

/// Available compiler commands.
#[derive(Subcommand)]
enum Command {
    /// Compile a Kodo source file to a binary.
    Build {
        /// The source file to compile.
        #[arg()]
        file: PathBuf,

        /// Output file path.
        #[arg(short, long)]
        output: Option<PathBuf>,

        /// Emit errors as JSON (for AI agent consumption).
        #[arg(long, default_value_t = false)]
        json_errors: bool,

        /// Contract checking mode: static, runtime, both, none, recoverable.
        #[arg(long, default_value = "runtime")]
        contracts: String,

        /// Print the MIR (Mid-level IR) to stdout before code generation.
        #[arg(long, default_value_t = false)]
        emit_mir: bool,
    },
    /// Lower a source file to MIR and print it without generating code.
    Mir {
        /// The source file to lower.
        #[arg()]
        file: PathBuf,

        /// Contract checking mode: static, runtime, both, none, recoverable.
        #[arg(long, default_value = "runtime")]
        contracts: String,
    },
    /// Type-check and verify contracts without generating code.
    Check {
        /// The source file to check.
        #[arg()]
        file: PathBuf,

        /// Emit errors as JSON.
        #[arg(long, default_value_t = false)]
        json_errors: bool,

        /// Contract checking mode: static, runtime, both, none, recoverable.
        #[arg(long, default_value = "runtime")]
        contracts: String,

        /// Emit a compilation certificate (`<file>.cert.json`) after a successful check.
        #[arg(long, default_value_t = false)]
        emit_cert: bool,

        /// Emit repair plans as JSON for each error (for AI agent consumption).
        #[arg(long, default_value_t = false)]
        repair_plan: bool,
    },
    /// Tokenize a source file and print the token stream.
    Lex {
        /// The source file to tokenize.
        #[arg()]
        file: PathBuf,
    },
    /// Parse a source file and print the AST.
    Parse {
        /// The source file to parse.
        #[arg()]
        file: PathBuf,
    },
    /// Explain an error code in detail with examples.
    Explain {
        /// The error code to explain (e.g., E0200).
        #[arg()]
        code: String,

        /// Output as JSON instead of human-readable format.
        #[arg(long, default_value_t = false)]
        json: bool,
    },
    /// Format a Kodo source file.
    Fmt {
        /// The source file to format.
        #[arg()]
        file: PathBuf,

        /// Check formatting without modifying the file.
        #[arg(long, default_value_t = false)]
        check: bool,
    },
    /// Show the code generated by intent resolution.
    IntentExplain {
        /// The source file containing intents.
        #[arg()]
        file: PathBuf,

        /// Output as JSON instead of human-readable format.
        #[arg(long, default_value_t = false)]
        json: bool,
    },
    /// Start the Kodo Language Server Protocol server.
    Lsp,
    /// Start an interactive REPL (Read-Eval-Print Loop).
    Repl {
        /// Output all results as JSON (for AI agent consumption).
        #[arg(long, default_value_t = false)]
        json: bool,
    },
    /// Generate a confidence report for a module.
    ConfidenceReport {
        /// The source file to analyze.
        #[arg()]
        file: PathBuf,

        /// Output as JSON instead of human-readable table.
        #[arg(long, default_value_t = false)]
        json: bool,

        /// Confidence threshold -- functions below this value are flagged (0.0-1.0).
        #[arg(long, default_value_t = 0.8)]
        threshold: f64,
    },
    /// Apply auto-fix patches to resolve compiler errors.
    Fix {
        /// The source file to fix.
        #[arg()]
        file: PathBuf,

        /// Show patches without applying them.
        #[arg(long, default_value_t = false)]
        dry_run: bool,
    },
    /// Inspect metadata embedded in a compiled Kodo binary.
    Describe {
        /// The compiled binary to inspect.
        #[arg()]
        binary: PathBuf,

        /// Output as raw JSON instead of human-readable format.
        #[arg(long, default_value_t = false)]
        json: bool,
    },
    /// Run tests in a Kodo source file.
    Test {
        /// The source file containing tests.
        #[arg()]
        file: PathBuf,

        /// Filter tests by name substring.
        #[arg(long)]
        filter: Option<String>,

        /// Output results as JSON for agent consumption.
        #[arg(long, default_value_t = false)]
        json: bool,

        /// Contract checking mode: static, runtime, both, none, recoverable.
        #[arg(long, default_value = "runtime")]
        contracts: String,
    },
    /// Generate a consolidated audit report (confidence + contracts + annotations).
    Audit {
        /// The source file to audit.
        #[arg()]
        file: PathBuf,

        /// Output as JSON (default is human-readable).
        #[arg(long, default_value_t = false)]
        json: bool,

        /// Contract checking mode: static, runtime, both, none, recoverable.
        #[arg(long, default_value = "runtime")]
        contracts: String,

        /// Policy string for CI/CD gates (exit 1 on violation).
        ///
        /// Comma-separated key=value criteria. Supported keys:
        /// - `min_confidence=<float>` -- minimum effective confidence for all functions
        /// - `contracts=all_verified` -- all contracts must be statically verified
        /// - `contracts=all_present` -- all functions must have at least one contract
        /// - `reviewed=all` -- all functions must have `@reviewed_by` annotation
        #[arg(long)]
        policy: Option<String>,
    },
}

fn main() {
    tracing_subscriber::fmt()
        .with_env_filter(
            tracing_subscriber::EnvFilter::try_from_default_env()
                .unwrap_or_else(|_| tracing_subscriber::EnvFilter::new("warn")),
        )
        .init();

    let cli = Cli::parse();

    let exit_code = match cli.command {
        Command::Build {
            file,
            output,
            json_errors,
            contracts,
            emit_mir,
        } => {
            commands::build::run_build(&file, output.as_deref(), json_errors, &contracts, emit_mir)
        }
        Command::Mir { file, contracts } => commands::misc::run_mir(&file, &contracts),
        Command::Check {
            file,
            json_errors,
            contracts,
            emit_cert,
            repair_plan,
        } => commands::check::run_check(&file, json_errors, &contracts, emit_cert, repair_plan),
        Command::Lex { file } => commands::misc::run_lex(&file),
        Command::Parse { file } => commands::misc::run_parse(&file),
        Command::Explain { code, json } => commands::misc::run_explain(&code, json),
        Command::Fmt { file, check } => commands::misc::run_fmt(&file, check),
        Command::IntentExplain { file, json } => commands::misc::run_intent_explain(&file, json),
        Command::Lsp => commands::misc::run_lsp(),
        Command::Repl { json } => {
            if json {
                repl::run_repl_json()
            } else {
                repl::run_repl()
            }
        }
        Command::ConfidenceReport {
            file,
            json,
            threshold,
        } => commands::misc::run_confidence_report(&file, json, threshold),
        Command::Fix { file, dry_run } => commands::check::run_fix(&file, dry_run),
        Command::Test {
            file,
            filter,
            json,
            contracts,
        } => commands::test::run_test(&file, filter.as_deref(), json, &contracts),
        Command::Describe { binary, json } => commands::misc::run_describe(&binary, json),
        Command::Audit {
            file,
            json,
            contracts,
            policy,
        } => commands::misc::run_audit(&file, json, &contracts, policy.as_deref()),
    };

    std::process::exit(exit_code);
}
